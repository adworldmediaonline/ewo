/**
 * Get GTIN for a product by SKU
 * @param sku - Product SKU
 * @param gtin - Direct GTIN if available in product data
 * @returns GTIN code or undefined
 */
export const getProductGTIN = (sku?: string, gtin?: string): string | undefined => {
  // If product already has GTIN, use it
  if (gtin) {
    return gtin;
  }

  // If SKU is available, try to map it to GTIN
  if (sku && GTIN_MAP[sku]) {
    return GTIN_MAP[sku];
  }

  return undefined;
};

